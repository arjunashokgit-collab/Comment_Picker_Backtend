const seedComments = async () => {
  try {
    const count = await Comment.countDocuments();
    if (count === 0) {
      console.log('[seedComments] Comment collection is empty. Seeding sample comments...');
      
      const documents = sampleComments.map(text => ({ text }));
      await Comment.insertMany(documents);
      
      console.log(`[seedComments] Successfully seeded ${documents.length} sample comments!`);
    } else {
      console.log(`[seedComments] Comment collection already has ${count} records. Skipping seed.`);
    }
  } catch (error) {
    console.error('[seedComments] Error seeding comments:', error.message);
  }
};

